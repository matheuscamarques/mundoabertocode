class ElementoMapa{
    x: any;
    y: number;
    cor: string;
    tam: number;
    

    constructor(posx : number,posy:number,cor : string ,tam :number){
        this.x =posx;
        this.y = posy;
        this.cor = cor;
        this.tam = tam;
    }

    draw(p : p5,image : p5.Image){
        p.fill(this.cor);
        p.stroke(this.cor);
        //noFill();
        //Image(img,0,0);
        //square(this.x,this.y,this.tam);
        p.rect(this.x,this.y,this.tam,this.tam);
    }
}

class BlocoAzul extends ElementoMapa{

    constructor(posx : number,posy:number){
        super(posx,posy,'blue',Mundo.tamanho_blocos);
    }
}

class BlocoVermelho extends ElementoMapa{

    constructor(posx : number,posy:number){
        super(posx,posy,'red',Mundo.tamanho_blocos);
    }
}


class BlocoAmarelo extends ElementoMapa{

    constructor(posx : number,posy:number){
        super(posx,posy,'yellow',Mundo.tamanho_blocos);
    }
}

class BlocoPreto extends ElementoMapa{

    constructor(posx : number,posy:number){
        super(posx,posy,'black',Mundo.tamanho_blocos);
    }
}

class SpriteBlock extends ElementoMapa{
  
    tamanho = Mundo.tamanho_blocos;
    constructor(public x : number,public y: number,public matrixX: number,public matrixY:number){
        super(x,y,'black',50);
    }

    draw (p:p5,img : p5.Image){
        p.fill(this.cor);
        p.square( this.x  , this.y , this.tam );
        //console.log("Chegou aqui!");
        p.image(img, 
             this.x  ,
             this.y ,
             this.tamanho,
             this.tamanho,
             16*this.matrixX,
             16*this.matrixY,
             16,
             16
             );
        
    }
}


class Wall extends SpriteBlock{
    constructor(x : number,y :number){
        super(x,y,8,0);
    }
}


